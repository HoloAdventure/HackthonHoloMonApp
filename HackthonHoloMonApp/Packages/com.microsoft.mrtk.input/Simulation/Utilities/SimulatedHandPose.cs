namespace Microsoft.MixedReality.Toolkit.Input.Simulation
{
    /// <summary>
    /// A set of pre-defined hand poses which are recognized by the input simulator.
    /// </summary>
    public enum SimulatedHandPose
    {
        /// <summary>
        /// Relaxed, fingers up, palm facing away from the camera.
        /// </summary>
        Neutral = 0,

        /// <summary>
        /// Relaxed, fingers up, palm facing the camera.
        /// </summary>
        PalmUp = 1
    }
}
